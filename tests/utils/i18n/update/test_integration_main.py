def safe_backup(path: Path):
    backup = path.with_suffix(".bak")
    if backup.exists():
        backup.unlink()
    if path.exists():
        path.rename(backup)
    return backup

def safe_restore(original: Path, backup: Path):
    if original.exists():
        original.unlink()
    if backup.exists():
        backup.rename(original)

